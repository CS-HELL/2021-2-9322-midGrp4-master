    private void confirmButtonInitiate(){
        try{

            char[] passwordChar = jPasswordField.getPassword();
            char[] verifyPasswordChar = jPasswordField1.getPassword();

            int idNumber = selectedEmployee.getID_Number();
            String Password = new String(passwordChar);
            String verifyPassword = new String(verifyPasswordChar);

            String lastName = jLastNameField.getText();
            String firstName = jFirstNameField.getText();
            String middleName = jMiddleNameField.getText();

            SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String newBirthdate = simpleDateFormat.format(jBirthdayChooser.getDate());

            String position = jPositionField.getText();
            String email = jEmailAddressField.getText();
            String gender = (String) jGenderBox.getSelectedItem();
            int age = Functions.calculateAge(newBirthdate);

            verifyEmployee = new Employee(idNumber, Password, lastName, firstName, middleName, newBirthdate, position, email, age, gender);

            if(selectedEmployee.EmployeeInformation_toString().equals(verifyEmployee.EmployeeInformation_toString())){
                JOptionPane.showMessageDialog(null, "There were no changes made.", "No changes were detected!", JOptionPane.WARNING_MESSAGE);
            }
            else{
                if(Password.equals(verifyPassword)){

                    int choice = JOptionPane.showConfirmDialog(null, "Data of ID Number: " + verifyEmployee.getID_Number() + " will be updated, continue?", "Verify.", JOptionPane.YES_NO_OPTION);

                    if (choice == 0){
                        updatedSelectedEmployee = verifyEmployee;

                        entryInterface.updateEmployee(updatedSelectedEmployee);
                        entryInterface.UpdateToLogs(employee, selectedEmployee);
                        JOptionPane.showMessageDialog(null, "Employee Number: " + updatedSelectedEmployee.getID_Number() + " details has been updated.", "Updated Employee Successfully!", JOptionPane.INFORMATION_MESSAGE);
                        jIDNumberBox.addActionListener(this::jIDNumberBoxActionPerformed);
                    }

                }
                else{
                    JOptionPane.showMessageDialog(null, "Please recheck your passwords.", "Password(s) do not match!", JOptionPane.WARNING_MESSAGE);
                }
            }

        }catch (Exception e) {
            e.printStackTrace();
        }

    }

