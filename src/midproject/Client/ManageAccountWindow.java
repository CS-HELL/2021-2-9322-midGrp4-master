    private void jIDNumberBoxActionPerformed(ActionEvent evt){
        String selectedIDNumber = Objects.requireNonNull(jIDNumberBox.getSelectedItem()).toString();

        try {
            if(!selectedIDNumber.equals("Select Employee")){
                enableTextBoxes();
                selectedEmployee = entryInterface.getEmployeeData(Integer.parseInt(selectedIDNumber));

                jLastNameField.setText(selectedEmployee.getLastName());
                jFirstNameField.setText(selectedEmployee.getFirstName());
                jMiddleNameField.setText(selectedEmployee.getMiddleName());

                SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
                Date date = simpleDateFormat.parse(selectedEmployee.getBirthdate());

                jPositionField.setText(selectedEmployee.getPosition());
                jGenderBox.setSelectedItem(selectedEmployee.getGender());

                jBirthdayChooser.setDate(date);
                jPasswordField.setText(selectedEmployee.getPassword());
                jPasswordField1.setText(selectedEmployee.getPassword());
                jEmailAddressField.setText(selectedEmployee.getEmailAddress());

            }
            else{
                disableTextBoxes();
                clearTextBoxes();
            }
        }catch (RemoteException | ParseException e) {
            e.printStackTrace();
        }
    }

